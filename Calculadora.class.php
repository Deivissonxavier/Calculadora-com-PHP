<?php
	class Calculadora{
		public function soma($valor1,$valor2){
			return $valor1+$valor2;
		}
		public function subtrair($valor1,$valor2){
			return $valor1-$valor2;
		}
		public function dividir($valor1,$valor2){
			return $valor1/$valor2;
		}
		public function multiplicar($valor1,$valor2){
			return $valor1*$valor2;
		}
	}
?>